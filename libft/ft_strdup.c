#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;
	int		i;
	size_t	size;

	size = 0;
	while (s1[size] != '\0')
		size++;
	res = malloc(sizeof(char) * (size + 1));
	if (!res)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
